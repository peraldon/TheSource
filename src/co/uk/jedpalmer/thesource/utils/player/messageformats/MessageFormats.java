package co.uk.jedpalmer.thesource.utils.player.messageformats;

/**
 * Created by peraldon on 30/06/2015.
 */
public enum MessageFormats {
    /**
     * Title - The main area in the top third of the players screen. Large lettering, so cannot be lots of information
     * Subtitle - Directly below Title, smaller sized font so can be smaller, but still restrictive
     * Actionbar - Just above where named items show their names in the hotbar. Can fit a lot more information here
     * TabTitle - Area of one line above and below player's TAB. This isn't wiped server - server
     * Chat - Chat, obviously
     */
    TITLE, SUBTITLE, ACTIONBAR, TABTITLE, CHAT


}
